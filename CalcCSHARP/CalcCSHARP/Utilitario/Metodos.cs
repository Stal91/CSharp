using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CalcCSHARP.Utilitario
{
    public class Metodos
    {
        public double Somando(int num1, int num2)
        {
            return num1 + num2;
        }

        public double Subtraindo(int num1, int num2)
        {
            return num1 - num2;
        }

        public double Multiplicando(int num1, int num2)
        {
            return num1 * num2;
        }
        public double Dividindo(int num1, int num2)
        {
            return num1 / num2;
        }

    }
}
